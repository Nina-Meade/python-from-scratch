# if statement resolves to 0 or a FALSE/TRUE value.

#  The else statement is an optional statement and
#  there could be at most only one else statement following if.

hungry = True
if hungry:
    print("FEED ME")

hungry = False
if hungry:
    print("FEED ME")
else:
    print("Im not hungry")

hungry = True
if hungry:
    print("FEED ME")
else:
    print("Im not hungry")
